#Write a find function
  # finds a character in a string
  # Returns true if found
  # returns false otherwise
  
def findIt(string, thing):
    index = 0
    while index < len(string) and string[index] != thing:
        index = index + 1
     
    if index < len(string):
        return index
    else:
        return -1

          
     
      
          
         